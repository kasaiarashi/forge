#[cfg(test)]
mod tests {
    use super::*;

    /// Empty pins TArray — ArrayNum = 0 → should parse to zero pins.
    #[test]
    fn parses_empty_pin_array() {
        let data = [0u8, 0, 0, 0]; // i32 = 0
        let result = parse_k2_node(&data, &[]);
        assert!(matches!(result, K2NodeData::Parsed { pins } if pins.is_empty()));
    }

    /// Short/garbage input should never panic and should return Unknown.
    #[test]
    fn garbage_input_returns_unknown() {
        let data = [0xFFu8, 0xFF, 0xFF, 0x7F]; // huge positive count → over threshold
        let result = parse_k2_node(&data, &[]);
        assert!(matches!(result, K2NodeData::Unknown { .. }));
    }

    #[test]
    fn truncated_after_count_returns_unknown() {
        let data = [1u8, 0, 0, 0]; // 1 pin, but no body follows
        let result = parse_k2_node(&data, &[]);
        assert!(matches!(result, K2NodeData::Unknown { .. }));
    }

    /// Array of a single null-pin slot — bNullPtr = 1 → should produce empty pin list (null entries dropped).
    #[test]
    fn null_pin_slot_is_dropped() {
        let mut data = Vec::new();
        data.extend_from_slice(&1i32.to_le_bytes()); // count
        data.extend_from_slice(&1u32.to_le_bytes()); // bNullPtr = true
        let result = parse_k2_node(&data, &[]);
        match result {
            K2NodeData::Parsed { pins } => assert!(pins.is_empty()),
            _ => panic!("expected parsed"),
        }
    }

    /// FString round-trip: length 5 = "hello\0".
    #[test]
    fn fstring_reads_ascii() {
        let mut data = Vec::new();
        data.extend_from_slice(&6i32.to_le_bytes());
        data.extend_from_slice(b"hello\0");
        let mut r = Reader::new(&data);
        assert_eq!(r.fstring().unwrap(), "hello");
    }

    /// Empty FText (None history, no invariant string) = 9 bytes.
    #[test]
    fn skip_ftext_empty() {
        let mut data = Vec::new();
        data.extend_from_slice(&0i32.to_le_bytes()); // flags
        data.push(0xFFu8); // HistoryType = -1 (None)
        data.extend_from_slice(&0u32.to_le_bytes()); // bHasCultureInvariantString = false
        let mut r = Reader::new(&data);
        r.skip_ftext().unwrap();
    }

    #[test]
    fn fname_out_of_range_errors() {
        let mut data = Vec::new();
        data.extend_from_slice(&99i32.to_le_bytes());
        data.extend_from_slice(&0i32.to_le_bytes());
        let mut r = Reader::new(&data);
        assert!(r.fname(&["only_one".to_string()]).is_err());
    }
}
